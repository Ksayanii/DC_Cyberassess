'use client';

import { useState } from 'react';
import { motion } from 'framer-motion';
import { Shield } from 'lucide-react';
import { AssessmentData, ScoreData, Recommendation } from '@/types/assessment';
import { calculateScores, generateRecommendations } from '@/utils/scoreCalculator';
import AssessmentForm from '@/components/AssessmentForm';
import ReportSection from '@/components/ReportSection';

export default function Home() {
  const [currentView, setCurrentView] = useState<'form' | 'report'>('form');
  const [scores, setScores] = useState<ScoreData | null>(null);
  const [recommendations, setRecommendations] = useState<Recommendation[]>([]);

  const handleAssessmentSubmit = async (data: AssessmentData) => {
    const calculatedScores = calculateScores(data);
    const generatedRecommendations = generateRecommendations(data, calculatedScores);
    setScores(calculatedScores);
    setRecommendations(generatedRecommendations);
    setCurrentView('report');
  };

  const handleReset = () => {
    setCurrentView('form');
    setScores(null);
    setRecommendations([]);
  };

  return (
    <main className="min-h-screen py-12 px-4">
      <div className="max-w-6xl mx-auto">
        {currentView === 'form' ? (
          <motion.div initial={{ opacity: 0 }} animate={{ opacity: 1 }} transition={{ duration: 0.8 }}>
            <div className="text-center mb-12">
              <motion.div
                initial={{ scale: 0 }}
                animate={{ scale: 1 }}
                transition={{ duration: 0.6, delay: 0.2 }}
                className="inline-flex items-center justify-center w-20 h-20 bg-gradient-to-r from-indigo-500 to-purple-600 rounded-full mb-6"
              >
                <Shield className="w-10 h-10 text-white" />
              </motion.div>
              <motion.h1
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.6, delay: 0.4 }}
                className="text-5xl font-bold text-white mb-4"
              >
                Cybersecurity Assessment
              </motion.h1>
              <motion.p
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.6, delay: 0.6 }}
                className="text-xl text-blue-100 max-w-2xl mx-auto"
              >
                Evaluate your digital security practices and get personalized recommendations to strengthen your online protection.
              </motion.p>
            </div>
            <AssessmentForm onSubmit={handleAssessmentSubmit} />
          </motion.div>
        ) : (
          scores && (
            <ReportSection scores={scores} recommendations={recommendations} onReset={handleReset} />
          )
        )}
      </div>
    </main>
  );
}
